
// Java program to illustrate
// bitwise operators
 
public class bitwise {
    public static void main(String[] args)
    {
        // Initial values
        int a = 5;
        int b = 7;
 
        // bitwise and
        System.out.println("a&b = " + (a & b));
 
        // bitwise or
        System.out.println("a|b = " + (a | b));
    }
}