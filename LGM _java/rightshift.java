
// Java Program to demonstrate
// Signed Left-Shift Operator
 
// Importing required classes
import java.io.*;
 
// Main class
class rightshift {
 
    // main driver method
    public static void main(String[] args)
    {
        int number = 2;
 
        // 2 bit left shift operation
        int Ans = number << 2;
 
        System.out.println(Ans);
    }
}